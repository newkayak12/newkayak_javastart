package varify.exam17;

public class Printer {

	
	
	public static int Println( int num )  {
		System.out.println(num);
		return num;
	}
	
	public static boolean Println( boolean logic )  {
		System.out.println(logic);
		return logic;
	}
	
	public static double Println( double num2 )  {
		System.out.println(num2);
		return num2;
	}
	
	public static String Println( String txt )  {
		System.out.println(txt);
		return txt;
	}
}
