package sec05.exam06_predicate;

public class Student {
	
	private String name, sex;
	private int scroe;
	
	
	public Student(String name, String sex, int scroe) {
		this.name = name;
		this.sex = sex;
		this.scroe = scroe;
	}


	

	public String getSex() {
		return sex;
	}


	public int getScroe() {
		return scroe;
	}
	
	
	
	

}
